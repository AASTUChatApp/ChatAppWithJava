    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
        if(evt.getSource() == registerBtn){
            String Fname = fname.getText();
            String email = emailField.getText();
            String password = String.valueOf(PasswordField.getPassword());
            String confPwd = String.valueOf(confirmPassField.getPassword());
            Boolean err  = false;

            if(Fname.isEmpty()){
                nameErr.setText("First name can't be empty");
                nameErr.setForeground(Color.red);
                err = true;
            }else{
                nameErr.setText("");
            }
                     //   nameErr.setText(Fname);

            if(email.isEmpty()){
                emailErr.setText("Email can't be empty");
                emailErr.setForeground(Color.red);
                err = true;
            }else{
                emailErr.setText("");
            }
          //  emailErr.setText(email);
            if(password.isEmpty()){
                pwdError.setText("Please insert password");
                pwdError.setForeground(Color.red);
                err = true;
            }else{
                pwdError.setText("");
            }

            if(password.length() < 8){
                pwdError.setText("Make your password minlength of 8 chars");
                pwdError.setForeground(Color.red);
                err = true;
            }else{
                pwdError.setText("");
            }
            if(password.isEmpty() || !password.equals(confPwd)){
                confErr.setText("Please confirm your password");
                confErr.setForeground(Color.red);
                err = true;
            }else{
                confErr.setText("");
            }
            if(!err){
                formContainer.setVisible(false);
                this.dispose();
                Socket socket = null;
                try{
                    saveToDatabase(Fname,email,password);

                    socket = new Socket("localhost",8002);
                    ChatAppUI chatAppUI = new ChatAppUI(socket,email);
                    this.setSize(400,400);
                    chatAppUI.setSize(400,400);
                    chatAppUI.setVisible(true);
                    chatAppUI.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    chatAppUI.readMessage();
                }catch (IOException e){
                    System.out.println(e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_registerBtnActionPerformed

