    private boolean saveToDatabase(String Fname, String email, String password){
        try{
            connection = DriverManager.getConnection(jdbc,username,pass);
            String sql = "INSERT INTO user(name,email,password) VALUES(?,?,?)";
            stmt = connection.prepareStatement(sql);
            stmt.setString(1,Fname);
            stmt.setString(2,email);
            stmt.setString(3,password);
            stmt.executeUpdate(); 
            return true;
        }catch(SQLException e){
            e.printStackTrace();
        }
        return false;
    }

